# Arithmetic Operators

a = 5 
b = 2

print(a + b) # Summation
print(a - b) # Subtraction 
print(a * b) # Multiplication
print(a / b) # Division
print(a % b) # Modulo = Remainder = Shesh
print(a ** b) # Power of B = 2

# Comparison / Relational Operators

c = 50
d = 44

print(c == d) # False
print(c != d) # True
print(c > d) # True
print(c >= d) # True
print(c < d) # False
print(c <= d) # False

